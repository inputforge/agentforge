const XCODE_EXTENSIONS = new Set([".nib", ".xcworkspacedata", ".xcuserstate"]);
const MAYBE_MINIFIED_EXTENSIONS = new Set([".js", ".css"]);
const SOURCE_MAP_EXTENSIONS = [".css.map", ".js.map"];
const PROTOBUF_EXTENSIONS = new Set([".py", ".java", ".h", ".cc", ".cpp", ".m", ".rb", ".php"]);
const APACHE_THRIFT_EXTENSIONS = new Set([
  ".rb",
  ".py",
  ".go",
  ".js",
  ".m",
  ".java",
  ".h",
  ".cc",
  ".cpp",
  ".php",
]);
const HAXE_EXTENSIONS = new Set([".js", ".py", ".lua", ".cpp", ".h", ".java", ".cs", ".php"]);
const HTML_EXTENSIONS = new Set([".html", ".htm", ".xhtml"]);

